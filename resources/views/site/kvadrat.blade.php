    <h1>
        Квадрат
    </h1>

    <img src="/img/kvadrat/kvadrat.gif" alt="Квадрат" align="right">

    <p align="justify">
        Квадрат представляет собой <a href="/">изделие металлопроката в виде прутков</a> или тонкой проволоки
        квадратного сечения. Обычно квадрат используется в качестве заготовки для дальнейшего изготовления сортового и
        <a href="/">фасонного металло проката</a>, а также для производства деталей механизмов: от втулок до шпинделей.
        В соответствии с ГОСТ 2591-88 квадрат, официально именуемый как "<a href="/">прокат квадратный</a>"
        изготавливается со сторонами от шести до двухсот миллиметров. Квадраты со сторонами более двухсот миллиметров
        изготавливаются нечасто и только по предварительному согласованию с Заказчиком.
    </p>

    <p align="justify">
        Существует два основных класса точности квадрата - класс Б (повышенной точности) и класс В (обычной точности).
        Класс точности отражает степень соответствия материала стандартам, заданным в нормативных документах. Квадраты
        повышенной точности используются при изготовлении деталей и там, где требуется особая выверенность конструкции.
        Квадраты класса В доступнее и подходят для <a href="/">изготовления фасонного металлопроката</a>. Чаще всего
        квадрат изготавливается в прутках, если ширина стороны больше 14 миллиметров, по согласованию с Заказчиком
        квадрат со стороной, меньшей 14 миллиметров включительно изготавливают и поставляют в прутках.
    </p>

    <p align="justify">
        В зависимости от требований квадрат изготавливают мерной длины, немерной длины и кратной мерной длины. Сейчас
        также изготавливается калиброванный квадрат по ГОСТ 8559-75 со стороной квадрата от трех до ста миллиметров, при
        этом квадрат сечением до пяти миллиметров производится и поставляется в мотках, свыше пяти миллиметров - в
        прутках. Квадрат сечением до 13 миллиметров может быть поставлен в мотках по согласованию с Заказчиком. В
        зависимости от своего назначения квадрат изготавливается мерной длины, немерной длины с остатком до 10% массы
        партии, кратной мерной длины и ограниченной длины в пределах немерной.
    </p>

    <p align="justify">
        Квадрат длиной от 2 до 12 метров изготавливается из углеродистой обыкновенного качества и низколегированной
        стали, для квадрата длиной от 2 до 6 метров используется качественная углеродистая и легированная сталь, а для
        выпуска квадрата длиной от 1,5 до 6 метров применяется высоколегированная сталь. Согласно требованиям стандарта
        предельные отклонения по длине квадрата мерной длины (включая кратную мерную длину) не могут превышать +3
        сантиметра для квадрата до 4 метров, +5 сантиметров для квадрата длиной от 4 до 6 метров и +7 сантиметров при
        длин более 6 метров.
    </p>

    <p align="justify">
        Заказчик также вправе потребовать, чтобы отклонения квадрата не превышали +4 сантиметров при длине от 4 до 7
        метров и затем +5 миллиметров на каждый метр длины. При определении качества квадрата также используется такое
        понятие как "кривизна", и, согласно ГОСТ, кривизна квадрата I класса не может превышать 0,5% длины для квадратов
        со стороной до 25 миллиметров включительно и 0,4 длины для квадратов с большей стороной. Для квадрата II класса,
        первое не определено, а второе составляет 0,5%. Кроме того, по требованию Заказчика кривизна квадрата может не
        превышать 0,2% от длины. Важно, учесть, что кривизна измеряется на расстоянии не менее 150 миллиметров от конца
        для квадратов длиной не менее метра.
    </p>

    <p align="justify">
        Для определения <a href="/">качества квадратного металлопроката</a> используется еще ряд показателей, один из
        которых - скручивание квадрата. При этом для квадрата сечением до 14 миллиметров скручивание не может превышать
        24 градусов (4 градуса, умноженные на длину в метрах), квадрат сечением от 14 до 50 миллиметров ограничивается
        скручиванием 18 градусов (3 градуса х длина в метрах), а для квадрата сечением более 50 миллиметров принято
        максимальное скручивание 15 градусов (3 градуса х длина в метрах).
    </p>

    <p align="justify">
        На все вопросы Вам с удовольствием ответят наши менеджеры по контактным телефонам.
    </p>


