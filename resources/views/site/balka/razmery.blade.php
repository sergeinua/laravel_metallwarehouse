    <h2>Тавровая</h2>
    <p align="justify">
        –производится согласно техническим условиям. Тавровая балка - удобная форма проката, имеет профиль разреза в
        виде буквы т. Используется для сварных работ, позволяют заменить сдвоенный уголок. Широко применяется для
        возведения тепличных, и других конструкций, предполагающих легкость. В настоящее время тавровый прокат больших
        размеров не производят, заменяя его, при необходимости, разделенными вдоль двутаврами.
    </p>
    <h2>Балка - швеллер</h2>
    <p align="justify">
        Так, в некоторых источниках называют швеллер - это изделие металлопроката, полки которого находятся с одной
        стороны относительно стенки профиля, образуя, таким образом п – образный профиль разреза.
    </p>
    <p align="justify">
        Существуют так же и другие виды сечения – полая и угловая.
    </p>
    <h2>Электросварная балка</h2>
    <p align="justify">
        Новым поколением двутавровой балки является <a href="?topic=03balka&amp;subtopic=10-elektrosvarnaya">электросварная
            балка</a>, которая не уступает ей по характеристикам, но проще в изготовлении и, соответственно, выигрывает
        у неё в цене.
    </p>

