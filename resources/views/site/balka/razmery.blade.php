    <h1>Балка</h1>
    <p align="justify">
        – еще один скромный труженик строительства, как и арматура, несет на себе
        колоссальные нагрузки, снимает и перераспределяет нагрузки на здания и сооружения, при этом, обычно, не видна
        обывателю закрытая другими перекрытиями. Балку применяют для возведения сложных промышленных конструкций,
        сооружений, например мостов, и в гражданском строительстве.
    </p>
    <p align="justify">
        Специальные её типы применяются в автомобильной промышленности, для производства машин и различных агрегатов,
        тяжелой техники.
    </p>
    <p align="justify">
        Как продукт современного производства она разнообразна и по материалу изготовления, и по форме, о чем мы и
        попробуем рассказать дальше. Её прочность дает возможность широко применять её в строительстве. Балка –
        универсальное средство для сооружения различных построек, при этом экономичное и по способам обработки, и по
        массе получаемых зданий.
    </p>
    <img src="/img/balka/balka-8239-89.gif" alt="Балка 8239-89">
    <img src="/img/balka/balka-26020.gif" alt="Балка 26020">
    <img src="/img/balka/balka-sto-20-93.gif" alt="Балка СТО АСЧМ 20-93">
    <p align="justify">
        Типов <a href="/">различного металлопроката</a> действительно огромное множество, и выбрать необходимый для
        ваших нужд помогут менеджеры ООО "УралМет". Они помогут и определиться с необходимым <a href="/">типом
            проката</a>, и минимизировать ваши затраты, сверяя выбранное с параметрами ГОСТов.
    </p>
    <p align="justify">
        Вообще говоря, балка - это брус, имеющий разное сечение, принимающий на себя и передающий нагрузки. Обычно
        вертикальную, но иногда и горизонтальную нагрузки (сейсмологические условия, ветер при определенной высоте
        зданий), она передает фундаментным основаниям, через несущие конструкции - подвесы, стены, колонны. Свойство,
        определяющее возможность такой функциональности - это работа на изгиб.
    </p>
    <p align="justify">
        Под этим термином чаще всего подразумевают двутавр, но существуют, так же: тавровая, полая, угловая и <a
                href="/">балка-швеллер</a>.
    </p>
    <h2>
        Двутавровая
    </h2>
    <p align="justify">
        Наиболее часто используемая балка двутавровая имеет н – образный профиль в разрезе, или две соединенные буквы т.
        Профиль в виде буквы т называют тавром. От сюда и называние "двутавровая" – две тавровые.
    </p>
    <p align="justify">
        Двутавр разделяется по форме полок, которые могут быть параллельны друг другу (согласно ГОСТ 26020-83) или быть
        с уклоном (согласно ГОСТ 8239-89).
    </p>
    <p align="justify">
        С параллельными гранями полок они маркируются следующим образом:: "б" – нормальные, "к" – колонные, "ш" -
        широкополочные балки.
    </p>
    <p align="justify">
        <a href="/">Балка</a> с уклоном граней полок, который может составлять от
        шести до двенадцати процентов, в свою очередь имеет два вида - обычный и специальный. Специальная с маркировкой
        "м" применяется для возведения подвесных путей. В сфере добычи полезных ископаемых, автомобилестроении и
        производстве прочих агрегатов часто применяется <a href="/">специальный балочный прокат</a> с маркировкой "с".
    </p>
    <p align="justify">
        Длина стандартной балки может быть мерной - от 400 мм до 12000 мм, немерной и кратной. Специальные имеет мерную
        длину - от 400 мм до 13000 мм, а также имеют такие стандарты длины, как: мерная с остатком и кратная мерная с
        остатков (где остаток не должен превышать пять процентов от массы партии). В зависимости от нужд потребителя,
        длина может варьироваться.
    </p>
    <p align="justify">
        Параметры точности одинаковы как для стандартных, так и для специальных изделий. Допустимые отклонения
        составляют от -3 до +5 процентов по весу и 0,2 процента от длины - по кривизне.
    </p>
    <p align="justify">
        Как и <a href="/">прочий металлопрокат</a>, она делится на несколько классов точности. Так для стандартной балки
        применимы классы точности Б и В, повышенной и обычной, соответственно. Для специальной классы точности А и В -
        высокой и обычной.</p>
