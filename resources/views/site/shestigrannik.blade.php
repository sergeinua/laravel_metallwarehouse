    <h1>Шестигранник</h1>

    <img src="/img/shestigrannik/shestigrannik.gif" alt="Шестигранник стальной" align="right">
    <p align="justify">
        Шестигранник, <a href="/">одно из изделий металлопроката</a>, используемое, в основном для производства
        крепежных деталей, таких как гайки, болты, а так же частей для машин и оборудования. Особенностью данного
        изделия является наличие у него шести равных граней, образующих равный <a href="/">шестигранник</a> в сечении.
    </p>
    <p align="justify">
        В зависимости от назначения, они могут изготавливаться из разных марок стали, различных по химическому составу,
        например по наличию никеля, хрома, или меди. Технология изготовления шестигранника - ковка, калибрование,
        горячекатный способ. Материал, применяемый для изготовления шестигранника может быть высококачественным или
        качественным.
    </p>
    <p align="justify">
        Основные параметры - длинна и диаметр (измеряется по внутреннему кругу). Длинна может быть мерной, кратной, и не
        мерной, обычно он представляет собой пруты от 2х до 6и метров. Диаметр может быть от 8 мм до 100 мм.
        Производственные мощности позволяют изготавливать шестигранник разной длинны, и разного диаметра, в зависимости
        от потребностей заказчика. Так же, в некоторых случаях, его фасуют не в кипы прутами, а мотками.
    </p>
    <p align="justify">
        По допустимым погрешностям - по длине, диаметру и кривизне - делятся на два класса точности Б и В, где Б - класс
        повышенной точности, а В - обычной.
    </p>
    <p align="justify">
        Выбор качества параметров требуемого шестигранника зависит от того, какой дальнейший метод обработки к нему
        будет применяться, например воздействие давлением, холодное волочение или механическая обработка холодного типа.
    </p>
    <p align="justify">
        Если Вам что-то осталось непонятно по <a href="/">классификации металло изделий из проката и шестигранника</a>,
        либо возникли любые вопросы - звоните нам по контактным телефонам.
    </p>



