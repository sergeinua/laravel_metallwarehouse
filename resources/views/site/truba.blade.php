    <p align="JUSTIFY">Как уже говорилось ранее, один из способов формирования шовной <a
                href="/">электросварной трубы</a> – закрепление шва
        посредством сварки. В этом случае
        заготовки – штрипсы или из <a href="/">листового металлопроката</a> – соединяют электросваркой, получаемый таким
        образом <a href="/">прокат
            называется электросварным</a>. Шов может быть как продольным, так и спиралевидным. Технология позволяет
        получать продукцию различной
        формы – квадрата, круга, овала. Так же, не ограничен и диаметр. </p>
    <p align="JUSTIFY">Изготовленные сварным способом изделия применяются для прокладки теплосетей. Срок эксплуатации ее
        довольно долог, а способность переносить нагрузки –
        высокая. Это позволяет применять её для прокладки конструкций магистрального типа. Чаще всего для <a
                href="/">изготовления электросварных труб</a>
        применяется сталь СТ1СП, СТ2СП, СТ2СП, СТ3СП или СТ20СП.</p>
