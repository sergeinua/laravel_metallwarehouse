    <h1>Труба</h1>

    <p align="JUSTIFY">Трудно представить себе современный мир без такого простого, и в то же время – гениального
        изобретения, <a href="/">как труба</a>.
        Она изготавливается из различных материалов, таких как: асбоцемент, железобетон, полимерные
        материалы, стойкое стекло, но чаще всего - <a href="/">из металлов</a>. Наиболее часто используется
        металлический <a href="/">трубный прокат - из стали разных
            марок</a>. Или, как вариант, из <a href="/">чугуна</a>.
    </p>
    <p align="JUSTIFY">Область применения трубного проката – практически безгранична. Начиная от привычных всем <a
                href="/">водо- и газопроводных, канализационных</a>, и заканчивая теми, которые используют в
        строительстве и при добыче полезных ископаемых.
    </p>
    <p align="JUSTIFY">Это конструкторское изобретение, настолько разнообразное, что может применяться в различных
        физических условиях. Например,
        для транспортировки, как уже говорилось, воды, газа, нефти и прочих веществ. Важно отметить, что некоторые их
        конструкции позволяют осуществлять
        транспортировку веществ, находящихся под давлением.</p>
    <p align="JUSTIFY">Изделия трубного проката, рассчитанные на применение в сложных условиях, могут быть устойчивы к
        температурным перепадам, химическим агрессивным средам,
        воздействию давления. Дополнительные материалы покрытия, технологии ее изготовления, а так же способы скрепления
        позволяют создавать изделия
        настолько многофункциональными. Они разнообразны и по материалу, из которого они сделаны. Кроме самых
        распространенных – стальных, ООО
        "Металлоторг" может предложить металлические из цветных металлов – алюминиевые , латунные , медные.</p>
    <p align="JUSTIFY">Не нужно забывать о том, что они назодят применение и в производстве бытовой и промышленной
        техники – для холодильного
        оборудования, кондиционирования, и т.д. Так же важна её роль в машиностроении. Множество агрегатов и узлов
        техники изготовлены именно в виде трубок
        различной конфигурации, выполненные из различных материалов. </p>
    <p align="JUSTIFY">Чаще всего, она представляет собой удлиненное изделие <a href="/">металлопроката</a>, полое
        внутри. Она имеет круглое, овальное
        или многоугольное сечение, в зависимости от технологических нужд. </p>
    <p align="JUSTIFY">Технология изготовления разнообразна. Существуют шовный и бесшовный способы изготовления. Швы
        формируются посредством сварки, в этом случае заготовки – штрипсы или <a href="/">из листового
            металло-проката</a> –
        соединяют электросваркой, отсюда и название – "<a href="/">электросварная</a>".
        Как вариант, шов может формироваться с помощью печной сварки.
        Так же шов может быть сформирован посредством паяния, или комбинированным, сварно-паяным способом, когда сварной
        шов пропаивают медью. По форме шов
        может быть двух типов – прямой – проходящий вдоль, или спиралевидный.</p>

    <p align="JUSTIFY">Стальная труба – наиболее часто используемая в строительстве, устройстве зданий и сооружений,
        прокладки коммуникаций.
        Разнообразие исполнений – по способу изготовления, по диаметру, толщине ее стенки, делает её <a href="/">универсальным
            изделием металлопроката</a>. </p>
