    <h2>Характеристика нержавеющей арматуры</h2>
    <p align="justify">Наиболее экономические выгодно использовать в строительстве продукцию из нержавеющей стали. Так к
        вашему вниманию наша компания готова предложить нержавеющую (аустенитную) арматуру. К основным достоинствам
        можно отнести не только само качество материала, но и используемые при производстве нержавеющей стали
        современные технологии. </p>
    <img src="/img/n03.jpg" width="250px">
    <p align="justify">Обычная арматура производится из низколегированной углеродистой стали, в которой содержится
        примерно 0,2% углерода, других добавок и элементом материал не имеет. Арматуры из нержавеющей стали имеет в
        своем составе разнообразные легирующие элементы, включая никель, хром и молибден, что приводит к значительному
        улучшению антикоррозийных свойств. Согласно характеристикам от производителя, в металле содержится 1,2% углерода
        и 10% хрома (минимальные значения). </p>
    <p align="justify">Нержавеющую сталь разделяют на группы, что зависит от микроструктуры материала:
    </p>
    <ul>
        <li>Мартенситная (ферритная), используемые не для проведения армирования, поскольку отличаются низким уровнем
            коррозийной стойкости
        </li>
        <li>Аустенитная, отличающаяся высоким уровнем коррозийной стойкости, прочностью, удобством формования. Этот тип
            используется чаще всего при армировании железобетонных сооружений.
        </li>
        <li>Дуплекс, сочетающий в себе одновременно свойства ферритной и аустенитной стали, однако требования к
            обработке этой стали значительно выше.
        </li>
    </ul><p></p>
