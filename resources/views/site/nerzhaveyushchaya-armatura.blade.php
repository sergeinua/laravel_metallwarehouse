        качества от 100 и более лет. </p>
    <h2>Нержавеющая сталь против коррозии.</h2><br>
    <p align="justify">Коррозия это самопроизвольное разрушение металлов в результате химического или физико-химического
        взаимодействия с окружающей средой. Причиной коррозии служит неустойчивость конструкционных материалов к
        воздействию веществ, находящихся в контактирующей с ними среде. В повседневной жизни для сплавов железа (сталей)
        чаще используют термин «ржавление». Скорость коррозии зависит от температуры. Повышение температуры на несколько
        градусов может значительно увеличить скорость коррозии. Для процесса коррозии следует применять термин
        «коррозионный процесс», а для результата процесса — «коррозионное разрушение». Наиболее часто при коррозии
        металл окисляется с образованием ионов металла, которые при дальнейших превращениях дают различные продукты
        коррозии. Коррозия приводит ежегодно к миллиардным убыткам, и решение этой проблемы является важной задачей.
        Основной ущерб заключается не в потере металла как такового, а в огромной стоимости изделий, разрушаемых
        коррозией. Вот почему ежегодные потери от неё в промышленно развитых странах столь велики. Истинные убытки от
        неё нельзя определить, оценив только прямые потери, к которым относятся стоимость разрушившейся конструкции,
        стоимость замены оборудования, затраты на мероприятия по защите от коррозии. Для предотвращения коррозии в
        качестве конструкционных материалов стараются применять продукцию из нержавеющей стали, так как нержавеющая
        сталь – это легированная сталь, устойчивая к коррозии и благодаря высокому содержанию хрома способная
        сопротивляться воздействию агрессивных сред в атмосфере.</p>
    <img src="/img/n02.jpg" width="250px">
    <p align="justify">Сопротивление нержавеющей стали к коррозии напрямую зависит от содержания хрома: при его
        содержании 13 % и выше сплавы являются нержавеющими в обычных условиях и в слабоагрессивных средах, более 17 % —
        коррозионностойкими и в более агрессивных окислительных и других средах, в частности, в азотной кислоте
        крепостью до 50 %. Причина коррозионной стойкости нержавеющей стали объясняется, главным образом, тем, что на
        поверхности хромсодержащей детали, контактирующей с агрессивной средой, образуется тонкая плёнка нерастворимых
        окислов, при этом большое значение имеет состояние поверхности материала, отсутствие внутренних напряжений и
        кристаллических дефектов. По химическому составу нержавеющие стали делятся на: хромистые (мартенситные,
        полуферритные, ферритные), хромоникелевые (аустенитные, аустенитно-ферритные, аустенитно-мартенситные,
        аустенитно-карбидные) и хромомарганцевоникелевые (аустенитные, аустенитно-ферритные, аустенитно-мартенситные,
        аустенитно-карбидные).</p>
    <p align="justify">Различают аустенитные нержавеющие стали, склонные к межкристаллитной коррозии, и
        стабилизированные — с добавками Ti и Nb. Значительное уменьшение склонности нержавеющей стали к межкристаллитной
        коррозии достигается снижением содержания углерода (до 0,03 %). Широкое распространение получили сплавы железа и
        никеля, в которых за счёт никеля аустенитная структура железа стабилизируется, а сплав превращается в
        слабо-магнитный материал. Основным преимуществом сталей аустенитного класса являются их высокие служебные
        характеристики (прочность, пластичность, коррозионная стойкость в большинстве рабочих сред) и хорошая
        технологичность. </p>
    <p align="justify">К такому классу относится марка стали AISI 304. Эта марка является одной из самых известных
        благодаря своей универсальности. К дополнительным преимуществам стали относят высокий уровень кислотостойкости и
        возможность выдерживать температуру, поднимающуюся до 900 градусов.</p>
