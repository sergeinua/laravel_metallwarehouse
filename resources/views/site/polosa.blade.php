    <h1>Металлопрокат полоса</h1>

    <img src="/img/polosa/polosa.gif" alt="Полоса стальная" align="right">

    <p align="justify">
        Стальная полоса, как и остальные <a href="/?topic=12list">листы стали</a>, используются для дальнейшего
        изготовления деталей, корпусов приборов, разнообразной техники, а чистом виде пригодны для кровельных и стеновых
        работ. Стальные горячекатаные полосы общего назначения, как и полосы для гаек шириной от 11 до 200 миллиметров и
        толщиной от 4 до 60 миллиметров, изготавливаются в соответствии с ГОСТ 103-76. По точности прокатки полосы
        бывают повышенной и нормальной точности. В зависимости от длины изготавливаемой полосы выбирается и материал:
        для полос длиной от 3 до 10 метров используется углеродистая обыкновенного качества, фосфористая и
        низколегированная сталь, для полос длиной от 2 до 6 метров требуется легированная и углеродистая качественная
        сталь.
    </p>

    <p align="justify">
        В зависимости от функционального назначения полосы могут быть мерной длины, мерной длины с остатком не более 15%
        массы партии, кратной мерной длины, кратной мерной длины с остатком не более 15% массы партии и немерной длины.
        Важно учесть, что остатком считаются полосы углеродистой обыкновенного качества, низколегированной и фосфористой
        сталей длиной не менее 2 метров и полосы из легированной и углеродистой качественной сталей длиной не менее
        метра.
    </p>

    <p align="justify">
        По согласованию с клиентов возможна поставка полос ограниченной длины в пределах немерной. Если поставляются
        полосы немерной длины, то возможна поставка полос от двух до трех метров из низколегированной, фосфористой и
        углеродистой стали обыкновенного качества и полос из углеродистой качественной и легированной стали длиной до
        метра до двух, но они не должны составлять более 10% массы партии. В условных обозначениях всегда указывается
        толщина, ширина, точность прокатки, материал изготовления и отклонения от стандарта.
    </p>

    <p align="justify">
        Так, обозначение 10Х22-В-Ш2 ГОСТ 103-76 Ст3кп ОСТ 14-2-208-87 означает, что перед нами стальная горячекатаная
        полоса толщиной 10 миллиметров и шириной 22 миллиметра, обычной точности прокатки (класс В), пригодная для
        холоднокатаной штамповки гаек (маркировка Ш), с серповидностью по классу 2 в соответствии с ГОСТ 103-76,
        изготовленная из стали Ст3кп. Предельные отклонения для перфорированных полос длиной до 4 метров составляют +30
        миллиметров, для перфорированных полос длиной от 4 до 6 метров - +50 миллиметров, для перфорированных полос
        длиной свыше 6 метров - +70 миллиметров и, наконец, для перфорированных полос, получаемых со штрипсовых станов,
        этот показатель составляет +200 миллиметров.
    </p>

    <p align="justify">
        В случае необходимости полосы размером до 30х20 миллиметров могут поставляться в рулонах, по требованию
        Заказчика возможно изготовление и поставка в рулонах полос иных размеров. Самым распространенным отклонением от
        стандарта является так называемая серповидность - небольшой равномерный изгиб полосы. Согласно требованиям
        стандарта серповидность полосы не может превышать 0,2% длины для оцинкованных полос первого класса и 0,5% длины
        для оцинкованных полос второго класса. Серповидность контролируется на длине не короче одного метра.
    </p>

    <p align="justify">
        По договоренности с клиентом возможна поставка полос с серповидностью до 0,8 процентов для любой измеряемой
        длины полосы. Притупление углов полосы не может быть более одной пятой части толщины и не более трех
        миллиметров. Такие параметры полосы, как толщина и ширина, должны измеряться на расстоянии не менее полуметра от
        конца полосы и на расстоянии трех метров от конца рулона. По требованию Заказчика можно изготавливать полосы
        толщиной 4,5 миллиметра любой ширины, предусмотренной ГОСТ 103-76, и ширины 56 миллиметров с толщиной до 36
        миллиметров включительно.
    </p>
