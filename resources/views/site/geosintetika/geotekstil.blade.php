    <h1>Геотекстиль</h1>
    <h2>
        Определение геотекстиля и область его применения
    </h2>
    <br>
    <p align="justify">
        Геотекстилем называется один из видов геосинтетического волокна, которое реализуется в рулонах. Производят этот
        материал из полипропиленового вещества, либо с нитей полиэфира, также допускается перемешивание между собой этих
        двух составляющих. При изготовлении геотекстиля из полипропилена допускается использование только первичного
        материала, в то время, как при производстве полиэфирных нитей применяют и первичное и вторичное сырье.
        Российские производители геотекстиля используют несколько технологий соединения между собой нитей. Наибольшее
        распространение получили технология пробивания иглой, а также термическое соединение волокон, осуществляемое под
        воздействием повышенных температур. Часто при использовании пробивной технологии, некоторые производители,
        дополнительно применяют и термическую обработку с помощью нагреты валов, между которых прокатывается волокно. В
        результате этого процесса получается геотекстиль, имеющий несколько названий: термообработанный иглопробивной,
        либо термоскрепленный. Также распространено название каландированный геотекстиль, т.к. нагретые валы,
        обрабатывающие волокно, называются каландрами.
    </p>
    <p align="justify">
        При изобретении геотекстиля, ему придавалось одно основное назначение – это создание непроницаемой прослойки,
        располагаемой между несколькими слоями почвы, чтобы они не перемешивались между собой. Однако возможности
        современного геотекстильного волокна далеко не ограничиваются этой функцией. Его широко применяют при покрое
        спец одежды, в строительной области, а также в некоторых производственных сферах.
    </p>
    <h2>
        Где используется геотекстиль
    </h2>
    <br>
    <p align="justify">
        Как уже отмечалось, геотекстильные материалы, в первую очередь используются для создания фильтрующего слоя в
        почве, который предотвращает ее эрозию, а также служит защитой используемой гидроизоляции от механического
        воздействия. При этом не происходит смещение почвы, а также ее перемешивание с другими рядом лежащими слоями.
        Используя геотекстиль в водопроводных системах, можно предотвратить их заливание и коррозию.
    </p>
    <p align="justify">
        Широкое распространение геотекстиль также получил в ландашфтном дизайне. Кроме того эти материалы активно
        используют в садоводстве и при благоустройстве земельных участков. Благодаря армированию почвы с помощью
        геотекстиля добиваются равномерного распределения нагрузок, поэтому почва долго сохранят свой первостепенный
        вид, а также увеличивается ее срок эксплуатации. При создании различных насыпей из земли в декоративных целях,
        геотекстиль способствует сохранению приданной формы на протяжении длительного времени. Происходит защита
        плодородных земель во время проведения рекультивации. Еще одна полезная характеристика геотекстиля . это защита
        плодородной земли от прорастания сорняков. Поэтому садоводы и дачники часто используют этот материал при
        выращивании различных культур.
    </p>
    <p align="justify">
        Сегодня геотекстиль все чаще используют при дорожном строительстве. Этот материал применяют в качестве
        разделения слоя почвы от слоя дорожного покрытия. Также часто применяется дорожный геотекстиль, когда происходит
        ремонт дорог . его укладывают поверх старого материала. Среди специалистов, занимающихся строительством и
        ремонтом дорог, часто данный материал именуется дорнитом или тряпкой.
    </p>
