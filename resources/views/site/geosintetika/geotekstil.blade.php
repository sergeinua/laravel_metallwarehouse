    <h2>
        Разновидности геотекстиля
    </h2>
    <br>
    <p align="justify">
        Геотекстиль подразделяется на множество групп по различным признакам. Основные из них . это способ изготовления,
        состав волокна и его структура.
    </p>
    <p align="justify">
        В зависимости от того, каким способом изготовлен геотекстиль, его подразделяют на следующие виды:
    </p>
    <ul>
        . Произведенный механическим способом за счет прокалывания специальными зазубренными иглами. Благодаря такому
        способу изготовления волокно становится более гибким и поэтому может растягиваться на 130% от первоначальной
        длины, что является очень высоким показателем. Из-за такой способности растягиваться, данный материал,
        практически никогда не разрывается при смещении почвы.
        . Термическое производство. Под воздействием высокой температуры волокна геотекстиля спаиваются между собой,
        образуя сплошную прочную структуру. Материал, произведенный таким способом, также обладает высокими
        эксплуатационными свойствам, однако в меньшей степени защищает от механических повреждений гидроизоляцию.
        . Химическое производство. Также этот метод производства часто называют адгезивным, т.к. при нем полимерные нити
        склеиваются между собой. Такой вид геотекстиля наиболее редкий из всех приведенных, и имеет специфические сферы
        использования.
    </ul>
    <p></p>
    <p align="justify">
        В зависимости от того, какие элементы входят в состав материала, геотекстиль подразделяется на следующие виды:
    </p>
    <ul>
        <li>На основе полипропилена. Сравнительно недавно появившись, этот материал отлично зарекомендовал себя у
            строителей. Он обладает отличными показателями сопротивления механическим и химическим воздействиям, а также
            великолепно пропускает через себя воду. Благодаря этим характеристикам, данному виду геотекстиля пророчат
            большое будущее.
        </li>
        <li>Полиэфирный геотекстиль. Данный материал является одним из самых распространенных и широко используемых.
            Обладает множеством достоинств, однако его применение ограничено в условиях действия кислот и щелочей, т.к.
            от действия этих химических составов, у полиэфира нет защиты.
        </li>
        <li>Полипропилен-полиэфирная смесь. Используются волокна и полипропилена и полиэфира, такие материалы имеют
            специфическую область применения и крайне редко используются.
        </li>
        <li>Изготовленный из вторичных, или как их еще называют, смесовых материалов. В качестве вторичных материалов
            могут применяться волокна из вискозы, шерсти, либо хлопчатобумажные составы. Данные материалы являются
            натуральными, поэтому в большей степени подвержены механическим и химическим воздействиям. В результате
            чего, смесовая геосинтетика в скором времени начинают гнить, разрушаться и ухудшать свои характеристики.
            Поэтому данный материал редко используется в строительстве (если только на не несущих нагрузки участках).
            Применяют его дачники и садоводы из-за низкой стоимости и доступности.
        </li>
    </ul>
    <p></p>


    <p align="justify">
        Геотекстиль . это высокопрочное текстильное тканое или нетканое плотно, изготовленное из полимерного материала с
        помощью иглопробивной технологии с последующей термообработкой, при которой волокна материала прочно и надежно
        склеиваются друг с другом. Относится к числу экологически безопасных геоситетических материалов.
    </p>
    <p align="justify">
        Геотекстиль используется при армировании участка, в качестве фильтрующей прослойки слоев грунта, при
        гидроизоляции, в дорожном и промышленном строительстве в качестве дренажа, и даже в ландшафтном дизайне для
        защиты растений от прорастания сорняков. Широкий спектр строительных работ использования геотекстиля приводит к
        огромному спросу данного материала на рынке строительных материалов, что обеспечивает сохранность и прочность
        конструкций и строительных объектов.
    </p>
    При укладывании между почвой и насыпным материалом геотекстиль  не дает рассыпаться насыпному материалу и проникать в грунт, что позволяет экономить в приобретении насыпного материала, и выполнять функцию качественного  и прочного армирования. Служит для укрепления склонов на неровных поверхностях и обеспечивает отведение дождевой воды, предотвращая эрозию почвенного настила.
