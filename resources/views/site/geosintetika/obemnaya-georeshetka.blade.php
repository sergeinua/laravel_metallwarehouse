    <p align="justify">
        Объемная георешетка представляет собой структуру, созданную из лент синтетического происхождения, которые
        сварены друг с другом. Сварка выполняется при помощи термической либо ультразвуковой технологии, благодаря чему
        образуется ячеистая структура волокна. В результате георешетка принимает форму сот, из-за чего ее часто называют
        геосотами. Такая объемная форма георешетки способствует тому, что ячейки могут быть заполнены любым строительным
        материалом, например, грунтом, песком или бетоном.
    </p>
    <p align="justify">
        Используют объемную георешетку в целях улучшения дренажа строительных материалов, кроме того объемные ячейки
        способствуют увеличению сцепления между крупными фракциями наполнителя, в результате чего образуется прочная
        цельная структура основания. Неперфорированная объемная георешетка чаще всего применяется в тех местах, где
        нужно укрепить горизонтально расположенное основание. Перфорированная разновидность георешетки более устойчива к
        поперечным нагрузкам, поэтому ее можно использовать для укрепления склонов.
    </p>
    <h2>Основное назначение объемной георешетки</h2><br>
    <p align="justify">
        Объемные георешетки используются в следующих случаях:
    </p>
    <ol>
        <li>Необходимо сделать дорожное покрытие, но при этом нет необходимости в дренаже сточных и дождевых вод.
            Фильтрация воды будет осуществляться за счет ее прохождения через верхний и нижний слои, расположенные с
            двух сторон от георешетки.
        </li>
        <li>Создание прочного основания на неустойчивом грунте.
        </li>
        <li>Предотвращает от эрозии откосы.
        </li>
        <li>Нагрузка, подаваемая на одну точку, равномерно распределяется по всей площади, на которой уложена объемная
            георешетка. За счет этого можно возводить тяжелые строительные объекты без опасности их проседания.
        </li>
        <li>Материалы, используемые для заполнения георешетки, становятся более устойчивыми к деформации за счет их
            плотного расположения в ячейках и отсутствия зазоров между ними.
        </li>
        <li>Предотвращаются боковые сдвиги и смещения, как самого наполнителя, так и георешетки в целом.
        </li>
        <li>Сокращается влияние внешних факторов на верхний слой георешетки.
        </li>
    </ol>
