    <h2>Инструкция по использованию объемной георешетки</h2><br>
    <p align="justify">
        Несмотря на множество преимуществ, которыми обладает объемная георешетка, часть ее полезных характеристик будет утрачена при неправильном выполнении монтажа. Поэтому необходимо тщательно изучить последовательность выполнения всех монтажных работ и точно выполнять все приведенные рекомендации.
    </p>
    <p align="justify">
        <b>Подготовка</b>
    </p><ul>
        <li>Необходимо исследовать почву, на которую будет укладываться объемная георешетка и рассчитать предполагаемые нагрузки. В зависимости от результатов расчета, выбирается нужный вид георешетки и условия ее монтажа. Площадка расчищается от всего мусора.
        </li><li>В качестве рекомендательного этапа можно предварительно перед слоем объемной георешетки монтировать нетканый геотекстиль. Однако при осуществлении защиты трубопроводов этот этап является обязательным. Благодаря наличию геотекстиля улучшаются дренирующие свойства всех слоев.
        </li></ul>
    <p></p>
    <b>Монтаж георешетки объемной</b>
    <ul>
        <li>Монтаж начинается с забивания Г-образных анкеров вдоль линии верхнего откоса. Данные анкеры могут быть, как металлическими, так и пластиковыми. Забиваются они не до конца, а их частота зависит от размера ячеек георешетки, а также от угла наклона откоса. Также в зависимости от того, насколько большой откос, необходимо выбирать соответствующую длину анкера.
        </li><li>Следующий этап монтажа . это растягивание решетки между анкерами и ее фиксация. После проверки, насколько ровно выполнен монтаж, анкера можно забить до конца в грунт.
        </li><li>Георешетка полностью раскатывается до нижнего края склона.
        </li><li>В том случае, если длины полотна не хватит до конца склона, нужно будет присоединить необходимой длин отрезок. При этом его монтаж осуществляется точно также, как и при установки первого участка . с помощью анкеров.
        </li><li>Соседние участки георешетки скрепляются между собой при помощи пневмостеплера, либо можно использовать те же анкера, которые применялись для горизонтального скрепления георешетки.
        </li><li>Модули георешетки фиксируются в шахматном порядке относительно центральной оси. Рекомендуемое количество модулей на один кв.м. . 3-4 штуки.
        </li><li>Количество и характеристики внутренних анкеров должно быть в соответствии с технической документацией.
        </li><li>После завершения всех модулей георешетки она проверяется на растяжение во всех направлениях.
        </li></ul>
    <b>Заполнение наполнителем</b>
    <ul>
        <li>В зависимости от площади установленной георешетки, заполнение осуществляется вручную, либо с использованием специальной техники.
        </li><li>На выбор размера ячеек георешетки оказывает влияние высота падения частиц наполнителя. Так, этот показатель для ячейки, величиной 40 на 40 см не должен быть больше 60 см, а при стандартных габаритах . 1 м.
        </li><li>Наполнять георешетку необходимо от верхнего края к нижней точке откоса.
        </li><li>При засыпании георешетки наполнителем, следует учитывать тот фактор, что он имеет свойство утрамбовываться. Поэтому необходимо заполнять ячейки георешетки с избытком. Например, при заполнении бетоном, необходимо уложить один слой, утрамбовать его, и следом положить еще один слой. Если же ячейки заполняются грунтом, его нужно положить как минимум на 25 см выше от кромки.
        </li><li>После окончания заполнения ячеек и проведения утрамбовки, заполнитель должен быть на одинаковом уровне с поверхностью георешетки.
        </li></ul>
