    <h2>Сварка геомембраны</h2><br>
    <p align="justify">
        Сварка рулонов геомембраны происходит следующим образом . два слоя укладываются внахлест, на них подается
        горячий воздух с аппарата, а следом проходит прижимной ролик, который плотно припаивает между собой мембраны,
        при этом образуется контрольный канал. При невозможности осуществления подобного вида сварки, используют ручной
        экструдер. В этом случае в качестве варочного элемента используется полимерная проволока, которая плавится и
        заполняет шов. После остывания обеспечивается прочное соединение полотен.
    </p>
    <h2>Проверка сварных швов на целостность</h2><br>
    <p align="justify">
        Проверка геомембраны на целостность в местах соединения можно совершать двумя способами. Первый заключается в
        подаче избыточного давления в контрольный канал . если будет утечка давления, значит на каком то участке есть
        отверстие или трещина. Второй способ . электроискровой заключается в пропускании тока через мембрану. Если есть
        разрыв, то электрический ток пройдет и будет видна искра.
    </p>
