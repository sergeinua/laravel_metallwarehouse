    <h1>Геомембрана</h1>

    <p align="justify">
        Геомембрана изготавливается из полиэтилена, в который добавляются в необходимых пропорциях антиокислители, сажа
        и стабилизаторы. Представляет собой рулонный строительный материал и используется в качестве гидроизоляции.
        Благодаря наличию в геомембране различных добавок и наполнителей, она обеспечивает отличные гидроизоляционные
        свойства, которые на порядок выше, чем в традиционных гидроизолирующих материалах. Существует два вида
        геомембран, отличающихся технологией производства. Первый производится под высоким давлением, но с низкой
        плотностью, его еще называют ПНД геомембраной. Второй, наоборот, производится под низким давлением и с высокой
        плотностью, его принято называть ПВД геомембраной. У каждого из этих строительных материалов есть свои
        недостатки и преимущества, обуславливающие их область применения. Так, ПНД геомембраны, или HDPE-геомембраны
        обладают высокой прочностью, однако при этом они менее гибкие и пластичные, чем мембраны ПВД или LDPE. Поэтому
        ПВД геомебрана применяется при монтаже на просадочных грунтах, либо на поверхностях, имеющих сложную форму. ПНД
        геомемраны используются для укладки на ровных поверхностях, на которые предполагается воздействие больших
        нагрузок. В зависимости от внешнего вида поверхности геомембран, их можно разделить также на два типа . с
        гладкой поверхностью, и с профилированной. Гладкая геомембрана обладает меньшей адгезией с грунтом, на который
        она укладывается, поэтому она применяется преимущественно на ровных поверхностях, где не предполагается
        продольное смещение. А профилированные мембраны за счет наличия выступов на своей поверхности, отлично
        сцепляются с грунтом, в результате чего их можно использовать на склонах. Кроме того, геомембраны с
        профилированной поверхностью, обеспечивают дренаж.
    </p>
