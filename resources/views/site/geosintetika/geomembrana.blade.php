    <h2>Геомемраны применяются для выполнения следующих функций:</h2><br>
    <p align="justify">
    </p>
    <ul>
        <li>Обеспечение гидроизоляции на горизонтальных поверхностях.
        </li>
        <li>Изоляция от влаги наклонных поверхностей и откосов.
        </li>
        <li>Предотвращение эрозии.
        </li>
        <li>Обеспечение вентиляции помещения через внутренние стены. Это свойство доступно только для перфорированных
            мембран.
        </li>
        <li>Армирование дорожных обочин для обеспечения их целостности.
        </li>
        <li>Равномерное распределение действующих нагрузок по всей площади грунта.
        </li>
        <li>Во время заливки фундаментов.
        </li>
        <li>Предотвращает негативное воздействие химических веществ на поверхность.
        </li>
    </ul>
