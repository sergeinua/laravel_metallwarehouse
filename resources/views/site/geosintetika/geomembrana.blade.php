    <h2>Процесс укладки геомембраны включает в себя следующие этапы:</h2><br>
    <p align="justify">
    </p>
    <ol>
        <li>Подготовительный этап: поверхность очищается от мусора, выдираются корни, а также выравниваются все
            неровности чтобы материал во время монтажа не был о них поврежден.
        </li>
        <li>Прежде чем укладывать геомембрану, предварительно насыпается песчаная подушка, толщина которой зависит от
            предполагаемой нагрузки и условий эксплуатации. Также, в случае необходимости защиты полотна от повреждений,
            монтируется слой геотекстиля.
        </li>
        <li>Рулоны геомембраны раскатываются по всей поверхности без натяжения, однако и наличие складок и неровностей
            не допускается.
        </li>
        <li>Между собой плотна геомембран соединяются при помощи экструдора ручного, либо аппаратами, генерирующими
            горячий воздух.
        </li>
        <li>Заключительный этап монтажа геомембраны включает проверку всего полотна на наличие повреждений и качество
            соединения швов.
        </li>
    </ol>
