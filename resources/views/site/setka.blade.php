        сетку и в сельском хозяйстве. Из нее часто обустраивают клети для животных, ограждения.</p>
    <p align="JUSTIFY">Сетку, достаточно гибкую, для удобства транспортирования сворачивают в рулоны. Изготовленную из
        толстых проволок или арматуры сетку поставляют в картах. Так, разделяют сетку сварную в рулонах и сетку сварную
