    <p align="JUSTIFY">Еще один параметр классификации сетки – это живое сечение, которое определяется как процентное
        отношение площади ячеек в свету к общей площади сетки. Соответственно, выделяют сетки с малым (25% всей
        площади), нормальным (25%–50%), большим (50%–75%) и особо большим (75%–100%) живым сечением.</p>
    <p align="JUSTIFY">Для улучшения эксплуатационных характеристик, таких как срок службы, сетку покрывают различными
        материалам: оцинковывают, или наносят полимерное напыление. </p>
    <h2>Использование:</h2>
    <p align="JUSTIFY"><strong>Стальную сетку</strong> применяют для обустройств несъемной опалубки, армирования
        конструкций из мелкозернистого бетона, в том числе при заливке полов, каменной кладке. Это позволяет придавать
        конструкции большее сопротивление и увеличивать срок службы. Стоит отметить, что железобетонные конструкции,
        армированные сеткой дольше не подвергаются коррозиям, а если на них возникаю трещины, то они не глубокие и
        равнораспределенные.</p>
    <p align="JUSTIFY">В дачном и коттеджном строительстве <b>сетку</b> используют для возведения легких заборов и
        оградительных сооружений. В опасных зонах сеткой покрывают крыши домов, и заземляют ее, устраивая, таким
        образом, громоотвод. В сельском хозяйстве применяют сетку для обустройства клеток и загонов для животных. В
        промышленности из нее делают ограждения и защитные экраны. </p>
    <p align="JUSTIFY">В строительстве, кроме армирования, сетку применяют для просеивания песка, гравия.</p>
    <p align="JUSTIFY">В современном строительстве и при отделочных работах часто используется так называемая <strong>штукатурная
            сетка</strong>, которая образуется с помощью перекрестного переплетения проволоки основы и проволоки утка.
        Штукатурные сетки бывают ткаными, ткаными с оцинкованным покрытием и просечно–вытяжными. Чаще всего встречаются
        тканые сетки с квадратными ячейками по <b>ГОСТ 3826-82</b>. </p>
    <p align="JUSTIFY">Штукатурная сетка используется для армирования штукатурного слоя, фильтрации, сушки,
        обезвоживания, отделения более крупных сыпучих тел и даже в качестве ленты на транспортере. Кроме того, тканые
        сетки с квадратными ячейками активно используются для воздухоочистки и изоляции труб.</p>
    <h2><u>Типы:</u></h2>
    <h3>Сетка рабица</h3>
    <p align="JUSTIFY"><img src="/img/setka/rabisa-1.gif" alt="Сетка рабица схема" align="left">
        Сетка <strong>рабица</strong> очень популярна. Первое, что приходит на ум обывателю при упоминании строительной
        сетки – это именно сетка, состоящая из изогнутых прутов проволоки, сплетенных между собой – <b>рабица</b>.
        Названа она так в честь своего изобретателя, немецкого инженера Карла Рабица. И назначение первой сетки было
        только лишь в штукатурном армировании.</p>
    <p align="JUSTIFY"><img src="/img/setka/rabisa.gif" alt="Сетка рабица" align="right">Сейчас <b>рабица</b> стала
        популярна, и практически все упомянутые выше области применения сетки справедливы и для нее. Для внутреннего
        армирования бетонных конструкций применяют <b>рабицу неоцинкованную</b>. Учитывая дешевизну такой рабици, часто
        именно ее применяют для просеивания сыпучих материалов и других технологических нужд. Для внешних работ
        применяют чаше <b>рабицу оцинкованную</b>. Для предания конструкции дополнительного эстетического эффекта, что
        актуально, например при возведении забор и прочих оградительных конструкций, применяют <b>рабицу с полимерным
            покрытием</b>. Для сплетения такой рабицы использую покрытые полимером проволоку. Рабица оцинкованная и не
        оцинкованная производится по <b>ГОСТ 5336-80</b> из сталей Ст1кп и Ст2кп.</p>
