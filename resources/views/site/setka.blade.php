    <h3>ЦПВС</h3>
    <p align="JUSTIFY"><img src="/img/setka/cvps-1.gif" alt="ЦПВС схема" align="left"><strong>Цельнометаллическая
            просечно-вытяжная cетка</strong>. В названии отражены практически все этапы производства. Металлическому
        листу (оцинкованный или нет) на специальном оборудовании делают просеки в шахматном порядке, и растягивают
        получившиеся ячейки в виде ромба. Такой способ производства сетки гораздо экономичней, чем выбивной, а
        просечно-вытяжная сетка по силе и стойкости не уступает ей. На последнем этапе производства сетку прокатывают,
        выравнивая ее края.</p>
    <p align="JUSTIFY"><img src="/img/setka/cvps.gif" alt="ЦПВС" align="right">Используют такую сетку в основном для
        армирования штукатурки, но не исключено использование ее и для ограждения (заборов, клетей и т.д.),
        теплоизоляции.</p>
    <p align="JUSTIFY">Основное преимущество такой сетки это то, что она легко сохраняет преданную ей форму, что
        актуально для армирования поверхностей с углами и множеством дизайнерских выступающих элементов.</p>
