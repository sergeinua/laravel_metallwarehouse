    <p align="JUSTIFY">Среди сварных сеток выделяют сетку сварную арматурную. Она достаточно прочна для того, что бы
        использовать ее для армирования железобетонных, каменных, кирпичнокладочных сооружений.</p>
    <p align="JUSTIFY"><strong>Сетка дорожная</strong></p>
    <p align="JUSTIFY">Сетку, используемую ее как при строительстве дорог, так и при проведении ремонтных работ на
        дорогах называют дорожной – сетка сварная дорожная. Способ крепления проволок между собой – точечная контактная
        сварка – усиливает конструкции. Проводка применятся пи этом ВР-1, диаметром от 4 до 5 мм.</p>
    <p align="JUSTIFY"><strong>Сетка кладочная</strong></p>
    <p align="JUSTIFY">Она достаточно крепка для того, что бы быть основой для кирпичной или каменной кладки. Такая
        сетка придает конструкции дополнительную устойчивость. Кладочная сетка – обычно неоцинкованная сварная
        сетка.</p><p>
    </p><h3>Тканная</h3>
    <p align="JUSTIFY"><img src="/img/setka/tkanaya-1.gif" alt="Сетка тканя схема" align="left">Такую сетку
        изготавливают на модернизированном ткацком станке – металлоткацком станке. Станок автоматический, и
        изготавливает <b>тканную сетку</b> из оцинкованной или неоцинокваной проволоки. Проволока для тканной сетки
        может быть как круглого, так и квадратного сечения. Как и в случае с тканями, проволока в сетке может
        переплетаться полотняным плетением или саржевым.</p>
    <p align="JUSTIFY"><img src="/img/setka/tkanaya.gif" alt="Сетка тканя" align="right">Фора ячейки может быть
        квадратной или прямоугольной. Или же, когда проволока укладывается встык, сетка получается без ячеек, т.н.
        фильтровочная сетка.</p>
    <p align="JUSTIFY">Применяют <b>тканную сетку</b> для армирований штукатурки – это предотвращает растрескивание.
        Тканые сетки изготавливаются путем взаимного переплетения проволоки и часто применяются для отсечки бетона,
        просеивания сыпучих материалов. Сетка с микронными ячейками, или без таких, может служить фильтром для
        газообразных и жидких составов. Так же тканная сетка – незаменимый материал для транспортировочных лент.</p>
