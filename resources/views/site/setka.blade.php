    <h1>Сетка металлическая проволочная</h1>

    <p align="JUSTIFY">Это строительный материал, представляющий собой переплетение тонких полос <a href="/">металла</a>
        (проволоки круглого или квадратного сечения, <a href="/">арматуры</a> и т.д.). <a href="/">Металл</a>,
        в местах соприкосновения, крепится разными способами, например сваркой или сплетением.</p>
    <b>По способам изготовления сетки подразделяются на следующие типы:</b>
    <ul>
        <li><strong>крученая</strong> – образованная методом попеременного скручивания каждой проволоки с одной из двух
            рядом расположенных;
        </li>
        <li><strong>плетеная</strong> – когда круглая или плоская спираль вплетается в предыдущую;</li>
        <li><strong>сборная</strong> – образуемая перекрестным переплетением гладких или предварительно деформированных
            проволок основы с проволоками утка;
        </li>
        <li><strong>сварная</strong> – из проволок или арматуры, расположенных перпендикулярно друг другу и сваренных в
            местах пересечения
        </li>
        <li><strong>тканая</strong> – где проволока основы (продольно полотну сетки) переплетается с проволокой утка
            (они проходят поперек полотна сетки);
        </li>
        <li><strong>ЦПВС</strong> – металлический лист с растянутыми до ромбов просеками.
        </li>
    </ul>
    <p align="JUSTIFY">По форме ячеек на свету выделяют сетки с квадратными ячейками, прямоугольными, ромбическими,
        шестигранными, трапециевидными и нулевыми.</p>
