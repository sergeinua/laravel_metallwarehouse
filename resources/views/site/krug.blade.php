    <h1>Круг</h1>

    <img src="/img/krug/krug.gif" alt="Круг стальной горячекатанный и холоднокатанный ГОСТ 2590-88" align="right">

    <p align="justify">
        Круг, официальное название согласно ГОСТ - <a href="/">металлопрокат стальной круговой</a>, на территории
        Украины используется двух основных стандартов: горячекатаный и калиброванная круглая сталь -
        холоднокатаная или холоднотянутая.
    </p>
    <p align="justify">
        Как калиброванный, так и горячекатаный круг, могут выпускаться не мерной, мерной кратной, и мерной длинны. И в
        том, и в другом случае, длина выпускаемой продукции может меняться по договоренности с потребителем.
    </p>
    <p align="justify">
        Кроме легированной, низколегированной, высоколегированной углеродистой сталей, применяемых для производства
        обоих стандартных круга, для производства калиброванной круга длинной от 2 до 6,5 метров, применяют еще и
        автоматную сталь. Общая тенденция сводится к тому, что качество применяемой для производства круга стали зависит
        от его длинны, и области дальнейшего применения.
    </p>
    <p align="justify">
        Точность круга, произведенного горячекатаным способом, разделяется на три класса - А, Б и В, соответственно,
        высокой, повышенной и обыкновенной точности. Для класса А допустимое отклонение от заданного параметра
        составляет от +0.3мм до -0.9мм. Для класса Б этот параметр лежит в пределах от +0.6мм до -2.0мм. Класс В
        позволяет отойти от стандарта на величину от +0.8мм до -4.0мм. Точный параметр допустимого отклонения зависит от
        присвоенного класса точности, а так же от длины изделия.
    </p>
    <p align="justify">
        Допустимое отклонение по длине калиброванного круга - от 30мм до 50мм, исходя из длинны изделия. Еще один
        параметр точности калиброванного круга - угол среза, который в идеале должен быть равен 90 градусам к основной
        оси прутка. Допустимое отклонение этого параметра зависит от диаметра изделия (может быть от 3мм до 100мм), и
        исчисляется как 20% от диаметра - для изделий диаметром менее 15мм, и 3мм-5мм для изделий диаметром 15мм и
        более.
    </p>
    <p align="justify">
        Диаметр горячекатаного круга может быть от 5мм до 270мм.
    </p>
    <p align="justify">
        Поставляют круг в мотках или прутках. В мотки формируют круг диаметром менее 25мм - для <a href="/">калиброванного
            металло-проката</a>, и менее 9мм - для горячекатного.
    </p>
    <p align="justify">
        Разобраться в сортаменте и узнать цены можно по контактным телефонам.
    </p>

