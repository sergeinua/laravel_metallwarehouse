    <p align="justify">Металлопрокат, как продукт промышленного производства, крайне необходим и широко
        применяем во всех сферах современной промышленности и быта. Бурный рост строительства жилых зданий и
        коммерческих сооружений напрямую ведет к увеличению спроса на металлические изделия. И приоритетом в
        выборе таких изделий сегодня, конечно же, является качество их производства, от которого зависит
        долговечность и безопасность объекта.</p>
    <p align="justify">По сравнению с другими альтернативными современными конструкционными материалами,
        металлопрокат выигрывает своими демократическими ценами и долговечностью использования. Среди
        огромного разнообразия металлической продукции новичку может быть достаточно непросто сразу
        разобраться в ассортименте изделий из металлопроката, но наша компания постарается Вам в этом
        помочь.</p>
    <p align="justify">Для начала нужно отметить различие по форме и размеру. В зависимости от нужд
        различных областей промышленности, металлопрокат выпускается разной формы и размера –
        крупногабаритные металлические изделия востребованы в строительной отрасли при возведении зданий, а
        более мелкие детали необходимы, как элементы различных механизмов, узлов в электротехнике, а так же
        машинах и агрегатах. Естественно, в таких сферах как аэрокосмическое приборо- и машиностроение,
        самолето-, автомобилестроение, а так же железнодорожное машино- и путестроение, металлопрокат
        является неотъемлемой и незаменимой частью.</p>
    <p align="justify">Наряду с геометрией изделий, важнейшим критерием выбора является марка стали, из
        которой выпускается металлопрокат. Это обусловлено такими сферами его применения, требующими
        определенного качества металла и его способностью приспособиться к явлениям окружающей среды –
        частым, либо существенным перепадам температур, агрессивности среды использования, направленным
        нагрузкам на растяжение и изгиб.</p>
