    <p align="justify">Изделия из металлопроката довольно легко крепятся при помощи разъемных либо
        неразъемных соединений, что является безусловным плюсом в выборе такой продукции. Благодаря широкому
        ассортименту, процесс создания устойчивых, долговечных и прочных конструкций значительно
        облегчается, что обусловлено популярностью среди специалистов строительной области следующих видов
        металлического проката:</p>
    <ul>
        <li>профнастил – незаменимый материал при проведении кровельных и
            отделочных работ;
        <li>листы и стальные полосы – так же
            используется при проведении кровельных и стеновых работ;
        <li>лента – нужна для бронирования, укрепления конструкций.
    </ul>
    <p align="justify">Так же металлопрокат, к примеру листовой, часто используется как исходный продукт для
        последующей обработки, к примеру при производстве:</p>
    <ul>
        <li>фасонный: двутавр, швеллер, уголок, арматура, балка;
        </li>
        <li>сортовой: квадрат, проволока, шестигранник, калиброванный круг, сетка (используется так же при просеивании и
            калибровке различных строительных материалов);
        </li>
        <li>периодический профиль: как правило это строительная арматура;
    </ul>
    <p align="justify">Все перечисленные выше разновидности металлопроката
        обычно подвергаются дальнейшей обработке, либо при проведении кровельных работ, монтажа лестничных
        пролетов и перекрытий зданий, а так же закладки фундамента.</p>
