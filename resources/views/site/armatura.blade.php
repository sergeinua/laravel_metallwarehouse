    <h1>Арматура. Арматурный прокат.</h1>
    <p align="justify">Строительная арматура – это металлический прут, с нанесенным на него повторяющимся сечением. Используют арматурный прокат для предания каким либо конструкциям большей устойчивости и силы сопротивления. Эффект достигается за счет того, что арматура связывает слои бетона и предупреждает появление трещин и сколов. Чаще всего, речь идет о железобетонных конструкциях и сооружениях. Но так ли их мало вокруг нас? </p>
    <p align="justify">Человеку, далекому от строительства, даже трудно представить, насколько часто в обыденной жизни он встречает арматуру. К счастью, прутья не торчат то тут, то там, но если присмотримся к окружающему внимательно, то понять не трудно – она – везде. </p>
    <p align="justify">Бордюрный камень вдоль дороги – ничто иное, как железобетонная конструкция, в основе которой – арматурный прокат. Лестничные пролеты, межэтажные перекрытия, а в случае, когда речь идет о панельном строительстве – и стены – все это внутри себя содержит металлическую основу. Незаменима она для монолитного возведения зданий. Кроме того, что <a href="/">металлопрокат – превосходная основа для железобетонных конструкций</a> и сооружений, его часто применяют и как самостоятельный материал, формируя из него, например, надежные металлические лестницы, ограждения и так далее. </p>
    <p align="justify">Не обходиться без неё и возведение крупных сооружений – мостов, тоннелей. Для этих целей применяю специальную арматуру, диаметр которой настолько велик, что бы выдерживать большие нагрузки. </p>
    <p align="justify">В зависимости от целей, с которой будет применяться арматурный прокат, она подбирается соответствующая. Во-первых, по назначению: анкерная, монтажная, конструктивная и рабочая. Во-вторых, по условиям применения: напрягаемая и не напрягаемая. В-третьих, по ориентации прута в конструкции: продольной или поперечной, в зависимости от потребности в том или ином усилении конструкции. Арматура, различная по назначению и условиям применения имеет свои особенности в строении, форме, марках и технологии стали, применяемой для ее изготовления. </p>
    <p align="justify">Учитывая что наиболее частое применение арматурного проката связано со взаимодействием с бетоном, отметим некоторые особенности, связанные с этим. Взаимодействовать бетон и сталь могут несколькими способами: трение, соединение на связках, обжатие металлопроката бетоном, сцепление, электрохимическое воздействие, основанное на реакции стали со специальными компонентами бетона. Разнообразны так же и способы соединения арматуры в бетоне: наиболее часто используемые связка проволокой, электросварка и использования специальных креплений.</p>
    <p align="justify">В ООО "Металлоторг" вы сможете купить различный по маркам стали, применяемым для изготовления, форме, технологии изготовления и <a href="/">техническим характеристикам металлопрокат</a>. </p>
