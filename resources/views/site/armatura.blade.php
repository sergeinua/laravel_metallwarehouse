    <p align="justify">Классифицируется по шести классам: арматура А1 (гладкие стержни), А2, А3, А4, А5 и А6 (включают стержни периодического профиля). </p>
    <ul>
        <li>А1 выпускается диаметром от 6мм до 40мм, изготавливается из сталей, марок 3СП, Д16, и Ст3 (СтЗкп; СтЗпс; СтЗсп;).
            Применяется в условиях, требующих от неё повышенного значения удлинения при растяжении, пластичности и хорошей переносимости низких температур. Применение класса А1 возможно для сварки несущих конструкций, сеток. </li>
        <li>А2 выпускается диаметром от 10мм до 80мм, изготавливается из сталей, марок:
            <ul>
                <li>Ст5сп; Ст5пс; - для класса А2 диаметром от 10мм до 40мм.</li>
                <li>18Г2С - для класса А2 диаметром от 40мм до 80мм.</li>
            </ul>
            Область применения <a href="/">прокатного</a> прута класса А2 мало отличается от области применения класса А1. </li>
        <li>А3 выпускается диаметром от 6мм до 40мм, изготавливается из сталей, марок: Ст3, 25Г2С, 35ГС, А400, А500С.
            Сталь, применяемая для изготовления данного класса обладает хорошей свариваемостью. Это делает пруты класса А3 пригодными для изготовления железобетонных конструкций, поэтому изделия этого класса диаметром 10мм, 12мм и 14мм - наиболее часто применяема в промышленности и гражданском строительстве. </li>
        <li>А4 выпускается диаметром от 10мм до 32мм,изготавливается из сталей, марок:
            <ul>
                <li>80С - для класса А4 диаметром от 10мм до 18мм.
                </li><li>20ХГ2Ц - для класса А4 диаметром от 10мм до 32мм.
                </li></ul>
            Арматура класса А4 и вышестоящих классов, применяется в роли напрягаемой. Но технические особенности прута класса А4 таковы, что его можно применять и для ненапряженных конструкций. Свойство свариваемости стали, применяемой для изготовления этого класса таково, что предпочтительней использовать для ее стыковки способ обжатой обоймы. </li>
        <li>А5 выпускается диаметром от 6мм до 36мм, для её изготовления применяется сталь марки 23Х2Г2Т (АТ800).
            Применяют <a href="/">металло-прокат такого класса</a> в условиях, пригодных для напрягаемых стержней, конструкциях, имеющих длинные пролеты. </li>
        <li>А6 выпускается диаметром от 6мм до 32мм, изготавливается из стали марки 22Х2Г2АЮ, 22Х2Г2Р, 20Х2Г2СР.
            Класс А6, как и класс А5 применяют в условиях, пригодных для напрягаемых стержней</li>
    </ul>
    <p align="justify">При изготовлении ей могут быть приданы дополнительные свойства, такие как: термохимическое упрочнение, упрочнение вытяжкой, термическая устойчивость. Информация об этом добавляется к маркировке. </p>
    <p align="justify">Для удобства маркировка наносится по краям прута, или на расстоянии пятидесяти сантиметров от края, и имеет цветовое обозначение. Арматура диаметром больше 10мм фасуется в прутках, меньше 10мм в – мотках. </p>
