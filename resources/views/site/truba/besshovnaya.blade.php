    <h1>Бесшовные трубы</h1>
    <p align="JUSTIFY">Бесшовные трубы производятся методом проката на специальных трубопрокатных станках. В результате
        данного процесса получаются трубы, которые не имеют на своей поверхности соединительного шва, как в
        электросварных трубах. Такая структура трубы обуславливает ее особые характеристики и область применения. Они
        обладают высокой прочностью, устойчивостью к разрыву, а также к механическим воздействиям. </p>
    <p align="JUSTIFY">По способу изготовления бесшовные трубы делятся на холоднокатаные, горячекатаные и цельнотянутые.
        Горячекатаные трубы широко применяются в качестве трубопроводов для транспортировки химических и активных
        веществ, протечка которых строго не допускается. Также такие трубы используют в машиностроении, нефтедобывающем
        производстве, коммунальных службах, а также в химической промышленности. </p>
    <p align="JUSTIFY">Цельнотянутые трубы можно отнести к подвиду толстостенного металлопроката. Кроме черного металла,
        допускается изготовление таких труб и из цветных металлов. Особенно актуальны при необходимости транспортировки
        внутри данных труб химически активных веществ. Также может использоваться в оборонной промышленности, а также
        при производстве судов и тяжелой техники. </p>
    <p align="JUSTIFY">Широкое распространение бесшовных труб обусловлено их повышенным эксплуатационным
        характеристикам, возможным благодаря отсутствию в них слабых мест соединения. Также цельный металлопрокат
        отличается улучшенным сопротивлением к агрессивным средам. </p>
    <p align="JUSTIFY">К способам изготовления бесшовных труб относят волочения, прессования или ковка. Горячекатаный
        способ от холоднодеформированного отличается использованием в первом варианте термической обработки.
        Производится данный металлопрокат из высокоуглеродистых, легированных и высоколегированных сталей, каждая из
        которых обуславливает область применения бесшовной трубы. </p>
    <p align="JUSTIFY">Изготавливаются на станках, где заготовки – металлические штанги – путем нагрева и <a href="/">проката
            на станках</a> принимают форму труб. Такие трубы обладают большей расчетной прочностью, по сравнению с
        трубами, имеющими шов. Но, технологически не возможно изготовление труб таким способом большого диаметра.</p>
    <p align="JUSTIFY">Для подгонки трубы под определенный <a href="/">размер трубы</a> подвергают доводке
        – прокатке и волочению.
    </p><h2><a name="truba-besshovnaya-goryachedeformirovannaya"></a> Труба бесшовная горячедеформированная </h2>
