        таких труб происходит по <b>ГОСТ 8732-78</b> и <b>ГОСТ 8731-87</b>. Материалом служит углеродистая или
        легированная сталь. </p>
    <p align="JUSTIFY"><strong>Бесшовную горячедеформированную трубу</strong> наиболее часто используют для прокладки
        жилищно-коммунальных коммуникаций. </p>
