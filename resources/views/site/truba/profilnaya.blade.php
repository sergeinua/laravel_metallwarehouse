    <h1>Профильные трубы</h1>
    <p align="JUSTIFY">Профильные трубы пользуются большой популярностью из-за широкой сферы своего применения –
        строительство, ремонт, бытовые потребности и пр. Профильными называют трубы, сечение которых отличается от
        круглого и может быть представлено в виде квадрата, прямоугольника или овала, менее распространены изделия с
        многоугольным профилем. Выпускаются в широком диапазоне размеров и толщин стенок, зависящих от области
        применения данных труб. Материал изготовления – черный металл, нержавеющая сталь или оцинкованный металл.
    </p><p align="JUSTIFY">Производятся профильные трубы двумя способами – первый заключается в изготовлении трубы путем
        сгибания листа в профильную форму и сваривание его по шву, второй представляет собой прокатывание круглой трубы
        специальными валиками для придания ей требуемой формы. Как правило данный металлопрокат выпускается хлыстами,
        размером 6 или 12 метров, однако бывают и исключения. Толщина стенок колеблется от 1.2 до 12 мм а ширина одной
        из стенок достигает 350 мм.
    </p><h2>Преимущества профильных труб</h2>
    <p align="JUSTIFY">Профильные трубы помимо высокой прочности и устойчивости к деформации обладают следующими
        преимуществами:
    </p>
    <ul>
        <li>стойкость к температурным изменениям
        </li>
        <li>малый коэффициент линейного расширения
        </li>
        <li>небольшой вес по сравнению с трубами круглого сечения
        </li>
        <li>сравнительно низкая стоимость<p></p>
        </li>
    </ul><p></p>
    <p align="JUSTIFY">Широкое распространение профильные трубы получили в строительной области. Они отлично подходят
        для сооружения различных несущих конструкций, т.к. обладают высокой прочностью. Применяются при сооружении
        следующих металлических конструкций и изделий:
    </p>
    <ul>
        <li>Изготовление основ для несущих конструкций
        </li>
        <li>Каркасы различных сооружений
        </li>
        <li>Стропильное перекрытие в промышленных зданиях
        </li>
        <li>Сооружение строительных лесов
        </li>
        <li>Строительство заборов, ворот</li>
    </ul><p></p>
    <p align="JUSTIFY">В зависимости от области применения профильные трубы подразделяются на машиностроительные,
        конструкционные, стандартные и термоустойчивые. Кроме стальных труб без покрытия, подверженных воздействию
        влаги, также выпускаются оцинкованные трубы, которые отличаются лучшими эксплуатационными характеристиками. </p>

    <p align="JUSTIFY">Кроме наиболее часто используемых <a href="/">труб</a> с круглым профилем,
        существуют трубы с квадратным, прямоугольным, овальным профилем. Некоторые заводы могут выпускать трубы
        специальных профилей с полыми ребрами, плоскоовальные и другие.</p>
