    <p align="justify">
        Горячая <a href="/">прокатка металла</a> заключается в том, что будущий <strong>уголок</strong> - заготовку в
        виде слитка - прокатывают через вращающиеся валы, где под воздействием давления формуется уголок. Гнутый уголок
        получают на профилегибочных агрегатах.</p>

    <p align="justify">
        Чаще всего уголки изготавливаются из углеродистой стали обычного качества, но могут быть изготовлены из
        низколегированной стали повышенной прочности.</p>

