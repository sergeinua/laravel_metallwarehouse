    <p align="justify">
        Используют уголок в строительстве и при монтаже разнообразных конструкций. Не уступает уголок <a
                href="/">балке</a> и <a href="/">швеллеру</a> по техническим
        характеристикам, и наравне с ними, применяется для укрепления несущих перегородок, организации проемов, усиления
        конструкций. Применение уголка в некоторых случаях выгодней по экономическим показателям, и удобней в некоторых
        типах конструкций.</p>


    <p align="justify">
        <b>Толщина полок уголка</b> может быть разнообразной, от 3мм. Длинна - обычно от 3м до 12м. По согласованию с
        Заказчиком длина уголка может быть иной.</p>


    <p align="justify">
        В случае необходимости уголки могут покрываться разнообразными пленками, обеспечивающими защиту от коррозии и
        преждевременного разрушения. </p>


    <p align="justify">
        Основным покрытием считается оцинковка, которая часто применяется там, где требуются нержавеющие уголки, также
        отдельно изготавливаются уголки из нержавеющей стали. Нержавеющие уголки обычно используются на объектах с
        повышенной влажностью, а оцинкованные применяются в пищевой и медицинской промышленности. Важно, что только
        оцинковка уголка позволяет избежать окисления металла и сохранить его стерильность в случае необходимости.</p>

